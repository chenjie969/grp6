package com.zjm.sys.db.model;

import java.io.Serializable;
import java.util.Date;

/**
 *	系统参数表
 * @author chenyang add 20170731
 *
 */
public class Sys_syspara implements Serializable{
	private static final long serialVersionUID = 1L;
	
	private String syspara_ID;//流水号
	private String praraID;//参数编码
	private String unit_uid;//机构id
	private String paraName;//参数名称
	private String paraValue;//参数值
	private String dataType;//数据值类型
	private Date updatedatetime;//最后更新时间
	private String update_user;//最后更新人
	private Integer isedit=1;//是否可编辑
	private String remarks;//备注
	
	

	//=============get/set===============
	
	public String getSyspara_ID() {
		return syspara_ID;
	}
	public void setSyspara_ID(String syspara_ID) {
		this.syspara_ID = syspara_ID;
	}
	public String getPraraID() {
		return praraID;
	}
	public void setPraraID(String praraID) {
		this.praraID = praraID;
	}
	public String getUnit_uid() {
		return unit_uid;
	}
	public void setUnit_uid(String unit_uid) {
		this.unit_uid = unit_uid;
	}
	public String getParaName() {
		return paraName;
	}
	public void setParaName(String paraName) {
		this.paraName = paraName;
	}
	public String getParaValue() {
		return paraValue;
	}
	public void setParaValue(String paraValue) {
		this.paraValue = paraValue;
	}
	public String getDataType() {
		return dataType;
	}
	public void setDataType(String dataType) {
		this.dataType = dataType;
	}
	public Date getUpdatedatetime() {
		return updatedatetime;
	}
	public void setUpdatedatetime(Date updatedatetime) {
		this.updatedatetime = updatedatetime;
	}
	public String getUpdate_user() {
		return update_user;
	}
	public void setUpdate_user(String update_user) {
		this.update_user = update_user;
	}
	public Integer getIsedit() {
		return isedit;
	}
	public void setIsedit(Integer isedit) {
		this.isedit = isedit;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	
}
