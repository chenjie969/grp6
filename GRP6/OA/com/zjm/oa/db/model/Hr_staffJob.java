package com.zjm.oa.db.model;

import java.io.Serializable;

public class Hr_staffJob implements Serializable {
	private String jobID; // JobID
	private String staffcase_Id;
	private String jobStartDate; // 工作开始时间(JobStartDate)
	private String jobEndDate; // 工作结束时间(JobEndDate)
	private String jobUnits; // 工作单位(JobUnits)
	private String jobAddress; // 工作地点(JobAddress)
	private String jobposts; // 工作职位(Jobposts)
	private String jobNotes; // 备注(JobNotes)
	public String getJobID() {
		return jobID;
	}
	public void setJobID(String jobID) {
		this.jobID = jobID;
	}
	public String getStaffcase_Id() {
		return staffcase_Id;
	}
	public void setStaffcase_Id(String staffcase_Id) {
		this.staffcase_Id = staffcase_Id;
	}
	

	public String getJobStartDate() {
		return jobStartDate;
	}
	public void setJobStartDate(String jobStartDate) {
		this.jobStartDate = jobStartDate;
	}
	public String getJobEndDate() {
		return jobEndDate;
	}
	public void setJobEndDate(String jobEndDate) {
		this.jobEndDate = jobEndDate;
	}
	public String getJobUnits() {
		return jobUnits;
	}
	public void setJobUnits(String jobUnits) {
		this.jobUnits = jobUnits;
	}

	public String getJobAddress() {
		return jobAddress;
	}
	public void setJobAddress(String jobAddress) {
		this.jobAddress = jobAddress;
	}
	public String getJobNotes() {
		return jobNotes;
	}
	public void setJobNotes(String jobNotes) {
		this.jobNotes = jobNotes;
	}
	public String getJobposts() {
		return jobposts;
	}
	public void setJobposts(String jobposts) {
		this.jobposts = jobposts;
	}
	
	
}
