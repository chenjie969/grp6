package com.zjm.oa.db.model;

import java.io.Serializable;
import java.util.Date;

/**
 * 
 * @author dj
 * 公告信息与人员的中间类
 */
public class Oa_message_user implements Serializable {

	private String  messageUserId;	 
	private String  messageId;  	//信息id,
	private boolean  isDuplicate;  	//是否抄送
	private String  userID;   			//人员id
	private String  userName;    //人员姓名
	private String    status; 			//01：待签收 02：已签收  
	private String  unit_uid;		//担保机构编号
	private String  unit_uidName;		//担保机构名称
	private String  updateUserName;			//最后修改人姓名
	private Date	updateDateTime;		//最后修改时间
	private boolean isRead;        //是否查阅
	
	
	public String getMessageUserId() {
		return messageUserId;
	}
	public void setMessageUserId(String messageUserId) {
		this.messageUserId = messageUserId;
	}
	public String getMessageId() {
		return messageId;
	}
	public void setMessageId(String messageId) {
		this.messageId = messageId;
	}
	public boolean isDuplicate() {
		return isDuplicate;
	}
	public void setDuplicate(boolean isDuplicate) {
		this.isDuplicate = isDuplicate;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getUnit_uid() {
		return unit_uid;
	}
	public void setUnit_uid(String unit_uid) {
		this.unit_uid = unit_uid;
	}
	public String getUnit_uidName() {
		return unit_uidName;
	}
	public void setUnit_uidName(String unit_uidName) {
		this.unit_uidName = unit_uidName;
	}
	public String getUpdateUserName() {
		return updateUserName;
	}
	public void setUpdateUserName(String updateUserName) {
		this.updateUserName = updateUserName;
	}
	public Date getUpdateDateTime() {
		return updateDateTime;
	}
	public void setUpdateDateTime(Date updateDateTime) {
		this.updateDateTime = updateDateTime;
	}
	public boolean isRead() {
		return isRead;
	}
	public void setRead(boolean isRead) {
		this.isRead = isRead;
	}
	
	                                                                                                                                                                                                  
	
}
