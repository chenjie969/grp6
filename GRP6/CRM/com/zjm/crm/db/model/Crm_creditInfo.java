package com.zjm.crm.db.model;

import java.io.Serializable;
import java.util.Date;

/**
 * 信用信息及银企往来情况crm_creditInfo
 * @author Administrator
 */
public class Crm_creditInfo implements Serializable {

	private String creditInfo_ID;	//流水号
	private String client_ID;	//客户ID
	private String bankCreditInfo;	//申请人及其关联公司在银行授信及使用情况
	private Double loanBalance;	//未结清贷款余额
	private String creditResult;	//人民银行信贷征信系统查询结果
	private String lawsuitInfo;	//申请人及其关联人、关联企业是否涉及诉讼、仲裁及执行情况等法律文件
	private String marketMessage;	//市场软消息（同行业评价、银行评价、周边熟悉人评价等）
	private Double garantySum;	//对外担保余额
	private String otherDesc;	//其它说明
	private String unit_uid;	//担保机构ID
	private String	unit_uidName;//机构名称
	private String updateUserName;	//最后修改人姓名
	private Date updateDateTime;	//最后修改时间
	
	public String getCreditInfo_ID() {
		return creditInfo_ID;
	}
	public void setCreditInfo_ID(String creditInfo_ID) {
		this.creditInfo_ID = creditInfo_ID;
	}
	public String getClient_ID() {
		return client_ID;
	}
	public void setClient_ID(String client_ID) {
		this.client_ID = client_ID;
	}
	public String getBankCreditInfo() {
		return bankCreditInfo;
	}
	public void setBankCreditInfo(String bankCreditInfo) {
		this.bankCreditInfo = bankCreditInfo;
	}
	public Double getLoanBalance() {
		return loanBalance;
	}
	public void setLoanBalance(Double loanBalance) {
		this.loanBalance = loanBalance;
	}
	public String getCreditResult() {
		return creditResult;
	}
	public void setCreditResult(String creditResult) {
		this.creditResult = creditResult;
	}
	public String getLawsuitInfo() {
		return lawsuitInfo;
	}
	public void setLawsuitInfo(String lawsuitInfo) {
		this.lawsuitInfo = lawsuitInfo;
	}
	public String getMarketMessage() {
		return marketMessage;
	}
	public void setMarketMessage(String marketMessage) {
		this.marketMessage = marketMessage;
	}
	public Double getGarantySum() {
		return garantySum;
	}
	public void setGarantySum(Double garantySum) {
		this.garantySum = garantySum;
	}
	public String getOtherDesc() {
		return otherDesc;
	}
	public void setOtherDesc(String otherDesc) {
		this.otherDesc = otherDesc;
	}
	public String getUnit_uid() {
		return unit_uid;
	}
	public void setUnit_uid(String unit_uid) {
		this.unit_uid = unit_uid;
	}
	public String getUpdateUserName() {
		return updateUserName;
	}
	public void setUpdateUserName(String updateUserName) {
		this.updateUserName = updateUserName;
	}
	public Date getUpdateDateTime() {
		return updateDateTime;
	}
	public void setUpdateDateTime(Date updateDateTime) {
		this.updateDateTime = updateDateTime;
	}
	public String getUnit_uidName() {
		return unit_uidName;
	}
	public void setUnit_uidName(String unit_uidName) {
		this.unit_uidName = unit_uidName;
	}
	
}
