package com.zjm.crm.db.model;

import java.io.Serializable;
import java.util.Date;

/**
 * 企业详情→企业财务状况→主要负债情况→贷款银行
 */
public class Crm_loanRec implements Serializable {

	private String loanRec_ID;	//流水号
	private String client_ID;	//客户ID
	private String loanBank;	//贷款银行（含小贷）
	private Double loanSum;	//贷款金额（万元）
	private String period;	//贷款期限
	private Date beginDate;	//开始日期
	private Date endDate;	//结束日期
	private String guarantyType;	//担保方式
	private String remark;	//备注
	private String unit_uid;	//担保机构ID
	private String unit_uidName;	//担保机构名称
	private String updateUserName;	//最后修改人姓名
	private Date updateDateTime;	//最后修改时间
	
	public String getLoanRec_ID() {
		return loanRec_ID;
	}
	public void setLoanRec_ID(String loanRec_ID) {
		this.loanRec_ID = loanRec_ID;
	}
	public String getClient_ID() {
		return client_ID;
	}
	public void setClient_ID(String client_ID) {
		this.client_ID = client_ID;
	}
	public String getLoanBank() {
		return loanBank;
	}
	public void setLoanBank(String loanBank) {
		this.loanBank = loanBank;
	}
	public Double getLoanSum() {
		return loanSum;
	}
	public void setLoanSum(Double loanSum) {
		this.loanSum = loanSum;
	}
	public String getPeriod() {
		return period;
	}
	public void setPeriod(String period) {
		this.period = period;
	}
	public Date getBeginDate() {
		return beginDate;
	}
	public void setBeginDate(Date beginDate) {
		this.beginDate = beginDate;
	}
	public Date getEndDate() {
		return endDate;
	}
	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}
	public String getGuarantyType() {
		return guarantyType;
	}
	public void setGuarantyType(String guarantyType) {
		this.guarantyType = guarantyType;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getUnit_uid() {
		return unit_uid;
	}
	public void setUnit_uid(String unit_uid) {
		this.unit_uid = unit_uid;
	}
	public String getUnit_uidName() {
		return unit_uidName;
	}
	public void setUnit_uidName(String unit_uidName) {
		this.unit_uidName = unit_uidName;
	}
	public String getUpdateUserName() {
		return updateUserName;
	}
	public void setUpdateUserName(String updateUserName) {
		this.updateUserName = updateUserName;
	}
	public Date getUpdateDateTime() {
		return updateDateTime;
	}
	public void setUpdateDateTime(Date updateDateTime) {
		this.updateDateTime = updateDateTime;
	}
}
