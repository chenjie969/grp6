package com.zjm.crm.db.model;

import java.io.Serializable;
import java.util.Date;

/**
 * 财务状况说明表
 */
public class Crm_financeDesc implements Serializable {

	private String financeDesc_ID;	//流水号
	private String client_ID;	//客户ID
	private String receivableDesc;	//近三年变动情况及原因（应收账款）
	private String otherReceivableDesc;	//近三年变动情况及原因（其他应收账款）
	private String inventoryDesc;	//近三年变动情况及原因（存货）
	private String longtermInvestDesc;	//长期投资
	private String fixedAssetsDesc;	//近三年固定资产投入变动情况
	private String loanChangeDesc;	//近三年贷款及或有负债变动情况
	private String incurDebtDesc;	//或有负债
	private String profitDesc;	//收入利润情况（开票销售、未开票销售、成本费用、利润率水平）
	private String unit_uid;	//担保机构id
	private String unit_uidName;	//担保机构名称
	private String updateUserName;	//最后修改人名称
	private Date updateDateTime;	//最后修改时间
	
	public String getFinanceDesc_ID() {
		return financeDesc_ID;
	}
	public void setFinanceDesc_ID(String financeDesc_ID) {
		this.financeDesc_ID = financeDesc_ID;
	}
	public String getClient_ID() {
		return client_ID;
	}
	public void setClient_ID(String client_ID) {
		this.client_ID = client_ID;
	}
	public String getReceivableDesc() {
		return receivableDesc;
	}
	public void setReceivableDesc(String receivableDesc) {
		this.receivableDesc = receivableDesc;
	}
	public String getOtherReceivableDesc() {
		return otherReceivableDesc;
	}
	public void setOtherReceivableDesc(String otherReceivableDesc) {
		this.otherReceivableDesc = otherReceivableDesc;
	}
	public String getInventoryDesc() {
		return inventoryDesc;
	}
	public void setInventoryDesc(String inventoryDesc) {
		this.inventoryDesc = inventoryDesc;
	}
	public String getLongtermInvestDesc() {
		return longtermInvestDesc;
	}
	public void setLongtermInvestDesc(String longtermInvestDesc) {
		this.longtermInvestDesc = longtermInvestDesc;
	}
	public String getFixedAssetsDesc() {
		return fixedAssetsDesc;
	}
	public void setFixedAssetsDesc(String fixedAssetsDesc) {
		this.fixedAssetsDesc = fixedAssetsDesc;
	}
	public String getLoanChangeDesc() {
		return loanChangeDesc;
	}
	public void setLoanChangeDesc(String loanChangeDesc) {
		this.loanChangeDesc = loanChangeDesc;
	}
	public String getIncurDebtDesc() {
		return incurDebtDesc;
	}
	public void setIncurDebtDesc(String incurDebtDesc) {
		this.incurDebtDesc = incurDebtDesc;
	}
	public String getProfitDesc() {
		return profitDesc;
	}
	public void setProfitDesc(String profitDesc) {
		this.profitDesc = profitDesc;
	}
	public String getUnit_uid() {
		return unit_uid;
	}
	public void setUnit_uid(String unit_uid) {
		this.unit_uid = unit_uid;
	}
	public String getUnit_uidName() {
		return unit_uidName;
	}
	public void setUnit_uidName(String unit_uidName) {
		this.unit_uidName = unit_uidName;
	}
	public String getUpdateUserName() {
		return updateUserName;
	}
	public void setUpdateUserName(String updateUserName) {
		this.updateUserName = updateUserName;
	}
	public Date getUpdateDateTime() {
		return updateDateTime;
	}
	public void setUpdateDateTime(Date updateDateTime) {
		this.updateDateTime = updateDateTime;
	}
	
}
