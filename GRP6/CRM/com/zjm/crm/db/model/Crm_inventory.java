package com.zjm.crm.db.model;

import java.io.Serializable;
import java.util.Date;

public class Crm_inventory implements Serializable {
	
    private String inventory_ID;//流水号
    
    private String client_ID;//客户ID
    
    private String inventoryName;//存货名称
    
    private Double inventoryCount;//存货数量
    
    private Double inventoryPrice;//目前市场单价
    
    private Double inventorySum;//存货金额（万元）
    
    private String remark;//备注
    
    private String unit_uid;//担保机构ID
    
    private String unit_uidName;//担保机构名称
    
    private String updateUserName;//最后修改人姓名
    
    private Date updateDateTime;//最后修改时间
    
    public String getInventory_ID() {
        return inventory_ID;
    }

    public void setInventory_ID(String inventory_ID) {
        this.inventory_ID = inventory_ID;
    }

    public String getClient_ID() {
        return client_ID;
    }

    public void setClient_ID(String client_ID) {
        this.client_ID = client_ID;
    }

    public String getInventoryName() {
        return inventoryName;
    }

    public void setInventoryName(String inventoryName) {
        this.inventoryName = inventoryName;
    }

    public Double getInventoryCount() {
		return inventoryCount;
	}

	public void setInventoryCount(Double inventoryCount) {
		this.inventoryCount = inventoryCount;
	}

	public Double getInventoryPrice() {
		return inventoryPrice;
	}

	public void setInventoryPrice(Double inventoryPrice) {
		this.inventoryPrice = inventoryPrice;
	}

	public Double getInventorySum() {
		return inventorySum;
	}

	public void setInventorySum(Double inventorySum) {
		this.inventorySum = inventorySum;
	}

	public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public String getUnit_uid() {
        return unit_uid;
    }

    public void setUnit_uid(String unit_uid) {
        this.unit_uid = unit_uid;
    }

    public String getUnit_uidName() {
        return unit_uidName;
    }

    public void setUnit_uidName(String unit_uidName) {
        this.unit_uidName = unit_uidName;
    }

    public String getUpdateUserName() {
        return updateUserName;
    }

    public void setUpdateUserName(String updateUserName) {
        this.updateUserName = updateUserName;
    }

    public Date getUpdateDateTime() {
        return updateDateTime;
    }

    public void setUpdateDateTime(Date updateDateTime) {
        this.updateDateTime = updateDateTime;
    }
}