package com.zjm.common.db.model;

import java.io.Serializable;

public class CheckResultInfo implements Serializable {
	private String fieldId;//控件id
	private String fieldValue;//控件value
	
	private Object otherId;
	
	public String getFieldId() {
		return fieldId;
	}
	public void setFieldId(String fieldId) {
		this.fieldId = fieldId;
	}
	public String getFieldValue() {
		return fieldValue;
	}
	public void setFieldValue(String fieldValue) {
		this.fieldValue = fieldValue;
	}
	public Object getOtherId() {
		return otherId;
	}
	public void setOtherId(Object otherId) {
		this.otherId = otherId;
	}
	
	
	
	
	
}
